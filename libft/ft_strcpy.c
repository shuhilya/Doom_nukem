#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int k;

	k = 0;
	while (*(src + k) != '\0')
	{
		*(dst + k) = *(src + k);
		k++;
	}
	*(dst + k) = '\0';
	return (dst);
}
