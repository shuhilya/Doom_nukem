#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int k;

	i = 0;
	if (ft_strlen(s2) == 0)
		return ((char*)s1);
	while (*(s1 + i) != '\0')
	{
		k = 0;
		while (*(s1 + i + k) == *(s2 + k) && *(s1 + i + k) != '\0')
			k++;
		if (*(s2 + k) == '\0')
			return ((char*)(s1 + i));
		i++;
	}
	return (0);
}
