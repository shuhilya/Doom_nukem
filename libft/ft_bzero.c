#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*c;

	c = s;
	i = 0;
	if (n > 0)
	{
		while (i < n)
		{
			*(c + i) = 0;
			i++;
		}
	}
}
